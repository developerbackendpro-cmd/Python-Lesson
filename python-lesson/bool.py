# | 1 |  bit_length() | True/False (1 yoki 0) sonini ifodalash uchun kerak bit  | –ö–æ–ª-–≤–æ –±–∏—Ç, –Ω—É–∂–Ω—ã—Ö –¥–ª—è –ø—Ä–µ–¥—Å—Ç–∞–≤–ª–µ–Ω–∏—è 1 –∏–ª–∏ 0             |
# | 2 |  conjugate()  | Kompleks sonlar uchun ishlatiladi (boolda ham mavjud)   | –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç –∫–æ–º–ø–ª–µ–∫—Å–Ω–æ-—Å–æ–ø—Ä—è–∂—ë–Ω–Ω–æ–µ —á–∏—Å–ª–æ (–¥–ª—è bool —Ç–æ–∂–µ)  |
