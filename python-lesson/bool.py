# üî¢ 5Ô∏è‚É£ bool (–ª–æ–≥–∏—á–µ—Å–∫–∏–π —Ç–∏–ø)
# bool ‚Äî –æ–±–æ–∑–Ω–∞—á–∞–µ—Ç –ª–æ–≥–∏—á–µ—Å–∫–∏–µ –∑–Ω–∞—á–µ–Ω–∏—è True –∏–ª–∏ False.
# –≠—Ç–æ—Ç —Ç–∏–ø —É–Ω–∞—Å–ª–µ–¥–æ–≤–∞–Ω –æ—Ç int, –ø–æ—ç—Ç–æ–º—É –æ–Ω —Ä–∞–±–æ—Ç–∞–µ—Ç –∞–Ω–∞–ª–æ–≥–∏—á–Ω–æ —Ü–µ–ª—ã–º —á–∏—Å–ª–∞–º.
#------------------------------------------------------------------------------------------------------------------------------------------#
