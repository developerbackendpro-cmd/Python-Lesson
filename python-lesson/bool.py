# –≠—Ç–æ —É–¥–æ–±–Ω–æ, –∫–æ–≥–¥–∞ –Ω—É–∂–Ω–æ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å True/False –∫–∞–∫ —á–∏—Å–ª–∞ ‚Äî –Ω–∞–ø—Ä–∏–º–µ—Ä, —Å–∫–ª–∞–¥—ã–≤–∞—Ç—å, —Å—á–∏—Ç–∞—Ç—å –±–∞–ª–ª—ã —Å–æ—Å—Ç–æ—è–Ω–∏—è –∏–ª–∏ –≤—ã–ø–æ–ª–Ω—è—Ç—å –º–∞—Ç–µ–º–∞—Ç–∏—á–µ—Å–∫–∏–µ –æ–ø–µ—Ä–∞—Ü–∏–∏.
