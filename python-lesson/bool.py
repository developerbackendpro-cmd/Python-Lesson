# True ‚Äî bu 1 ga teng. Shuning uchun 1 + 5 = 6.
# True —Ä–∞–≤–Ω—è–µ—Ç—Å—è 1. –ü–æ—ç—Ç–æ–º—É 1 + 5 = 6.
#------------------------------------------------------------------------------------------------------------------------------------------#
print(False * 10)
# 0
# False ‚Äî bu 0 ga teng. 0 * 10 = 0, natija 0 bo‚Äòladi.
# False —Ä–∞–≤–Ω—è–µ—Ç—Å—è 0. 0 * 10 = 0, —Ä–µ–∑—É–ª—å—Ç–∞—Ç ‚Äî 0.
#------------------------------------------------------------------------------------------------------------------------------------------#
# bit_length() ‚Äì int'nikidek ishlaydi
print(True.bit_length())
# 1
# True qiymati aslida 1 bo‚Äòlgani uchun, uning bit_length() funksiyasi butun sonlardagidek ishlaydi.
# 1 sonini ikkilik sanoq sistemasida yozish uchun 1 bit yetadi ‚Üí natija 1.
# True —Ñ–∞–∫—Ç–∏—á–µ—Å–∫–∏ —è–≤–ª—è–µ—Ç—Å—è —á–∏—Å–ª–æ–º 1, –º–µ—Ç–æ–¥ bit_length() —Ä–∞–±–æ—Ç–∞–µ—Ç —Ç–∞–∫ –∂–µ, –∫–∞–∫ —É —Ü–µ–ª—ã—Ö —á–∏—Å–µ–ª.
# –ß—Ç–æ–±—ã –ø—Ä–µ–¥—Å—Ç–∞–≤–∏—Ç—å —á–∏—Å–ª–æ 1 –≤ –¥–≤–æ–∏—á–Ω–æ–º –≤–∏–¥–µ, –Ω—É–∂–µ–Ω –≤—Å–µ–≥–æ 1 –±–∏—Ç ‚Üí —Ä–µ–∑—É–ª—å—Ç–∞—Ç 1.
#------------------------------------------------------------------------------------------------------------------------------------------#
# conjugate() ‚Äî kompleks teskari qiymat
direction = 3 + 4j   # dron sharqqa 3 birlik va shimolga 4 birlik harakatlanmoqda / –¥—Ä–æ–Ω –¥–≤–∏–∂–µ—Ç—Å—è –Ω–∞ –≤–æ—Å—Ç–æ–∫ –Ω–∞ 3 –µ–¥–∏–Ω–∏—Ü—ã –∏ –Ω–∞ —Å–µ–≤–µ—Ä –Ω–∞ 4 –µ–¥–∏–Ω–∏—Ü—ã
reverse_direction = direction.conjugate()
print("( Asl yo'nalish ) - ( –ü–µ—Ä–≤–æ–Ω–∞—á–∞–ª—å–Ω—ã–π –º–∞—Ä—à—Ä—É—Ç )", direction)
print("( Teskari yo'nalish ) - ( –û–±—Ä–∞—Ç–Ω–æ–µ –º–∞—Ä—à—Ä—É—Ç )", reverse_direction)
# ( Asl yo‚Äònalish ) - ( –ü–µ—Ä–≤–æ–Ω–∞—á–∞–ª—å–Ω—ã–π –º–∞—Ä—à—Ä—É—Ç ) (3+4j)
# ( Teskari yo‚Äònalish ) - ( –û–±—Ä–∞—Ç–Ω–æ–µ –º–∞—Ä—à—Ä—É—Ç ) (3-4j).
#------------------------------------------------------------------------------------------------------------------------------------------#
# Asl yo‚Äònalish: 3 + 4j
# ‚Üí dron sharqqa 3, shimolga 4 birlik harakat qiladi.
# Teskari yo‚Äònalish: 3 ‚Äì 4j
# ‚Üí dron sharqqa 3, lekin endi shimol emas ‚Äî janubga 4 birlik harakat qiladi.
# Demak, conjugate() kompleksi ‚Äúoynadagi aksiga‚Äù aylantiradi.
#------------------------------------------------------------------------------------------------------------------------------------------#
# –ù–∞—á–∞–ª—å–Ω–æ–µ –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏–µ: 3 + 4j
# ‚Üí –¥—Ä–æ–Ω –¥–≤–∏–∂–µ—Ç—Å—è –Ω–∞ –≤–æ—Å—Ç–æ–∫ –Ω–∞ 3, –Ω–∞ —Å–µ–≤–µ—Ä –Ω–∞ 4.
# –û–±—Ä–∞—Ç–Ω–æ–µ –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏–µ: 3 ‚Äì 4j
# ‚Üí —Ç–µ–ø–µ—Ä—å –æ–Ω –¥–≤–∏–∂–µ—Ç—Å—è —Ç–∞–∫ –∂–µ –Ω–∞ –≤–æ—Å—Ç–æ–∫, –Ω–æ –Ω–∞ —é–≥ –Ω–∞ 4.
# –ú–µ—Ç–æ–¥ conjugate() –æ—Ç—Ä–∞–∂–∞–µ—Ç –∫–æ–º–ø–ª–µ–∫—Å–Ω–æ–µ —á–∏—Å–ª–æ –ø–æ –≥–æ—Ä–∏–∑–æ–Ω—Ç–∞–ª—å–Ω–æ–π –æ—Å–∏ ‚Äî –∫–∞–∫ –∑–µ—Ä–∫–∞–ª–æ.
#------------------------------------------------------------------------------------------------------------------------------------------#
direction = 5 + 2j  # dron sharqqa 5 birlik, shimolga 2 birlik ketmoqda / –¥—Ä–æ–Ω –¥–≤–∏–∂–µ—Ç—Å—è –Ω–∞ –≤–æ—Å—Ç–æ–∫ –Ω–∞ 5 –µ–¥–∏–Ω–∏—Ü –∏ –Ω–∞ —Å–µ–≤–µ—Ä –Ω–∞ 2 –µ–¥–∏–Ω–∏—Ü—ã
horizontal = direction.real
print("( Gorizontal-harakat ) - ( –ì–æ—Ä–∏–∑–æ–Ω—Ç–∞–ª—å–Ω–æ–µ - –¥–≤–∏–∂–µ–Ω–∏–µ )", horizontal)
# ( Gorizontal-harakat ) - ( –ì–æ—Ä–∏–∑–æ–Ω—Ç–∞–ª—å–Ω–æ–µ - –¥–≤–∏–∂–µ–Ω–∏–µ ) 5.0
#------------------------------------------------------------------------------------------------------------------------------------------#
# Kompleks yo‚Äònalish: 5 + 2j
# dron sharqqa 5 birlik
# shimolga 2 birlik ketmoqda
# direction.real ‚Üí 5.0
# Ya‚Äôni dronning gorizontal (sharq/g‚Äòarb) bo‚Äòyicha harakati.
# real() ‚Üí kompleks sonning faqat haqiqiy (gorizontal) qismini qaytaradi.
#------------------------------------------------------------------------------------------------------------------------------------------#
# –ö–æ–º–ø–ª–µ–∫—Å–Ω–æ–µ –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏–µ: 5 + 2j
# –¥—Ä–æ–Ω –¥–≤–∏–∂–µ—Ç—Å—è –Ω–∞ –≤–æ—Å—Ç–æ–∫ –Ω–∞ 5 –µ–¥–∏–Ω–∏—Ü
# –∏ –Ω–∞ —Å–µ–≤–µ—Ä –Ω–∞ 2 –µ–¥–∏–Ω–∏—Ü—ã
# direction.real ‚Üí 5.0
# –¢–æ –µ—Å—Ç—å –≥–æ—Ä–∏–∑–æ–Ω—Ç–∞–ª—å–Ω–æ–µ –¥–≤–∏–∂–µ–Ω–∏–µ –¥—Ä–æ–Ω–∞.
# real() –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Ç–æ–ª—å–∫–æ —Ä–µ–∞–ª—å–Ω—É—é (–≥–æ—Ä–∏–∑–æ–Ω—Ç–∞–ª—å–Ω—É—é) —á–∞—Å—Ç—å –∫–æ–º–ø–ª–µ–∫—Å–Ω–æ–≥–æ —á–∏—Å–ª–∞.
#------------------------------------------------------------------------------------------------------------------------------------------#
direction = 5 + 2j  # dron sharqqa 5 birlik, shimolga 2 birlik ketmoqda / –¥—Ä–æ–Ω –¥–≤–∏–∂–µ—Ç—Å—è –Ω–∞ –≤–æ—Å—Ç–æ–∫ –Ω–∞ 5 –µ–¥–∏–Ω–∏—Ü –∏ –Ω–∞ —Å–µ–≤–µ—Ä –Ω–∞ 2 –µ–¥–∏–Ω–∏—Ü—ã
vertical = direction.imag
print("( Vertikal harakat ) - ( –í–µ—Ä—Ç–∏–∫–∞–ª—å–Ω–æ–µ –¥–≤–∏–∂–µ–Ω–∏–µ )", vertical)
# ( Vertikal harakat ) - ( –í–µ—Ä—Ç–∏–∫–∞–ª—å–Ω–æ–µ –¥–≤–∏–∂–µ–Ω–∏–µ ) 2.0
#------------------------------------------------------------------------------------------------------------------------------------------#
# Kompleks yo'nalish: 5 + 2j
# dron sharqqa 5 birlik, shimolga 2 birlik ketmoqda.
# direction.imag ‚Üí 2.0
# Ya'ni dronning vertikal (shimol/janub) bo‚Äòyicha harakati.
# imag() kompleks sonning mavhum qismi ‚Äî ya‚Äôni shimol/janub harakatini qaytaradi.
#------------------------------------------------------------------------------------------------------------------------------------------#
# –ö–æ–º–ø–ª–µ–∫—Å–Ω–æ–µ –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏–µ: 5 + 2j
# –¥—Ä–æ–Ω –¥–≤–∏–∂–µ—Ç—Å—è –Ω–∞ –≤–æ—Å—Ç–æ–∫ –Ω–∞ 5 –µ–¥–∏–Ω–∏—Ü,
# –Ω–∞ —Å–µ–≤–µ—Ä –Ω–∞ 2 –µ–¥–∏–Ω–∏—Ü—ã.
# direction.imag ‚Üí 2.0
# –¢–æ –µ—Å—Ç—å –≤–µ—Ä—Ç–∏–∫–∞–ª—å–Ω–æ–µ –¥–≤–∏–∂–µ–Ω–∏–µ –¥—Ä–æ–Ω–∞.
# imag() –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –º–Ω–∏–º—É—é —á–∞—Å—Ç—å –∫–æ–º–ø–ª–µ–∫—Å–Ω–æ–≥–æ —á–∏—Å–ª–∞ ‚Äî –¥–≤–∏–∂–µ–Ω–∏–µ —Å–µ–≤–µ—Ä/—é–≥.
#------------------------------------------------------------------------------------------------------------------------------------------#
flying = False       # dron uchmayapti / –¥—Ä–æ–Ω –Ω–µ –ª–µ—Ç–∏—Ç
charging = True      # dron hozir zaryad olayapti / –¥—Ä–æ–Ω —Å–µ–π—á–∞—Å –∑–∞—Ä—è–∂–∞–µ—Ç—Å—è
# numerator() -> True = 1, False = 0
status_score = flying.numerator + charging.numerator
print("(Dron status balli) - ( –°—Ç–∞—Ç—É—Å–Ω—ã–π –±–∞–ª–ª –¥—Ä–æ–Ω–∞ )", status_score)
if status_score == 0:
    print("( Dron hozir ham uchmayapti, ham zaryadda emas ) - ( –î—Ä–æ–Ω —Å–µ–π—á–∞—Å –Ω–∏ –ª–µ—Ç–∏—Ç, –Ω–∏ –∑–∞—Ä—è–∂–∞–µ—Ç—Å—è )")
elif status_score == 1:
    if flying:
        print("( Dron hozir uchmoqda ) - ( –¥—Ä–æ–Ω –ª–µ—Ç–∏—Ç )")
    elif charging:
        print("( Dron hozir zaryad olayapti ) - ( –î—Ä–æ–Ω —Å–µ–π—á–∞—Å –∑–∞—Ä—è–∂–∞–µ—Ç—Å—è )")
#------------------------------------------------------------------------------------------------------------------------------------------#
# numerator() ‚Äî bu bool qiymatni son sifatida olish uchun ishlatiladigan metod.
# True aslida 1
# False esa 0
# Shuning uchun:
# True.numerator ‚Üí 1
# False.numerator ‚Üí 0
# Demak, numerator() bool qiymatni matematik kasr shaklida ko‚Äòrib, uning suratini qaytaradi.
# Bu bizga True/False qiymatlarni qo‚Äòshish, hisoblash, holat ballari chiqarish kabi amallarda son sifatida ishlatishga yordam beradi.
#------------------------------------------------------------------------------------------------------------------------------------------#
# numerator() ‚Äî —ç—Ç–æ –º–µ—Ç–æ–¥, –∫–æ—Ç–æ—Ä—ã–π –ø–æ–∑–≤–æ–ª—è–µ—Ç –ø–æ–ª—É—á–∏—Ç—å –ª–æ–≥–∏—á–µ—Å–∫–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –∫–∞–∫ —á–∏—Å–ª–æ.
# –í Python:
# True —Ñ–∞–∫—Ç–∏—á–µ—Å–∫–∏ —Ä–∞–≤–Ω–æ 1
# False ‚Äî 0
# –ü–æ—ç—Ç–æ–º—É:
# True.numerator ‚Üí 1
