#-------------------------------------------------------------------------------------------------------------------------------#
# Python’dagi set — bu takrorlanmaydigan, tartibsiz elementlar to‘plami.
# Set ichida har bir element noyob bo‘ladi, ya’ni dublikatlarga ruxsat berilmaydi.
# Set в Python — это неупорядоченная коллекция уникальных элементов.
# Внутри множества каждый элемент является уникальным, то есть дубликаты не допускаются.
#-------------------------------------------------------------------------------------------------------------------------------#
# ✅ 1. add()
# Yangi element qo‘shadi
# 1. add()
# Добавляет новый элемент.
sonlar = {1, 2}
sonlar.add(3)
print(sonlar)
# {1, 2, 3}
#-------------------------------------------------------------------------------------------------------------------------------#
# ✅ 2. clear()
# To‘plamni bo‘shatadi
# ✅ 2. clear()
# Очищает множество.
mevalar = {'olma', 'banan'}
mevalar.clear()
print(mevalar)
# set()
#-------------------------------------------------------------------------------------------------------------------------------#
# ✅ 3. copy()
# Set nusxasini yaratadi
# 3. copy()
# Создаёт копию множества.
asli = {1, 2, 3}
yangi = asli.copy()
print(yangi)
# {1, 2, 3}
#-------------------------------------------------------------------------------------------------------------------------------#
# ✅ 4. discard(x)
# Elementni o‘chiradi. Topilmasa — xatolik chiqarmaydi.
# ✅ 4. discard(x)
# Удаляет элемент. Если элемент не найден — ошибку не вызывает.
mevalar = {'olma', 'banan'}
mevalar.discard('olma')
mevalar.discard('nok')  # mavjud emas, lekin xatolik yo‘q / # элемента нет, но ошибки нет
print(mevalar)
# {'banan'}
#-------------------------------------------------------------------------------------------------------------------------------#
# ✅ 5. remove(x)
# Elementni o‘chiradi. Topilmasa — xatolik chiqaradi.
# ✅ 5. remove(x)
# Удаляет элемент. Если элемент не найден — вызывает ошибку.
mevalar = {'olma', 'banan'}
mevalar.remove('banan')
print(mevalar)
# {'olma'}
#-------------------------------------------------------------------------------------------------------------------------------#
# ✅ 6. pop()
# Tasodifiy elementni o‘chiradi va qaytaradi
# ✅ 6. pop()
# Удаляет и возвращает случайный элемент.
sonlar = {1, 2, 3}
o = sonlar.pop()
print(sonlar)
# {2, 3}
#-------------------------------------------------------------------------------------------------------------------------------#
# ✅ 7. update()
# Setni boshqa iterabledagi elementlar bilan kengaytiradi
# ✅ 7. update()
# Расширяет множество элементами из другого итерируемого объекта.
a = {1, 2}
a.update([3, 4])
print(a)
# {1, 2, 3, 4}
#-------------------------------------------------------------------------------------------------------------------------------#
# ✅ 8. union()
# Ikkita setni birlashtiradi (takrorlanmas)
# 8. union()
# Объединяет два множества (без дубликатов).
a = {1, 2}
b = {2, 3}
print(a.union(b))
# {1, 2, 3}
#-------------------------------------------------------------------------------------------------------------------------------#
# ✅ 9. intersection()
# Umumiy (kesishuvchi) elementlarni qaytaradi
# ✅ 9. intersection()
# Возвращает общие (пересекающиеся) элементы.
a = {1, 2, 3}
b = {2, 3, 4}
print(a.intersection(b))
# {2, 3}
#-------------------------------------------------------------------------------------------------------------------------------#
# ✅ 10. difference()
# Faqat `a` da bor, `b` da yo‘q elementlar
# ✅ 10. difference()
# Элементы, которые есть только в a, но отсутствуют в b.
a = {1, 2, 3}
b = {2, 4}
print(a.difference(b))
# {1, 3}
#-------------------------------------------------------------------------------------------------------------------------------#
# ✅ 11. symmetric_difference()
# Ikkalasida ham bor, lekin umumiy bo‘lmagan elementlar
# ✅ 11. symmetric_difference()
# Элементы, которые присутствуют в обоих множествах, но не являются общими.
a = {1, 2, 3}
b = {2, 4}
print(a.symmetric_difference(b))
# {1, 3, 4}
#-------------------------------------------------------------------------------------------------------------------------------#
# ✅ 12. intersection_update()
# Faqat umumiy elementlarni qoldiradi
# ✅ 12. intersection_update()
# Оставляет только общие элементы.
a = {1, 2, 3}
b = {2, 3, 4}
a.intersection_update(b)
print(a)
# {2, 3}
#-------------------------------------------------------------------------------------------------------------------------------#
# ✅ 13. difference_update()
# `a` dan `b`dagi elementlarni o‘chiradi
# ✅ 13. difference_update()
# Удаляет из a элементы, которые присутствуют в b.
a = {1, 2, 3}
b = {2, 4}
a.difference_update(b)
print(a)
# {1, 3}
#-------------------------------------------------------------------------------------------------------------------------------#
# ✅ 14. symmetric_difference_update()
# Umumiy bo‘lganlarni olib tashlab, boshqa elementlar bilan yangilaydi
# ✅ 14. symmetric_difference_update()
# Удаляет общие элементы и обновляет множество остальными.
a = {1, 2, 3}
b = {2, 4}
a.symmetric_difference_update(b)
print(a)
# {1, 3, 4}
#-------------------------------------------------------------------------------------------------------------------------------#
# ✅ 15. isdisjoint()
# Umumiy element yo‘qmi?
# ✅ 15. isdisjoint()
# Проверяет, нет ли общих элементов.
a = {1, 2}
b = {3, 4}
print(a.isdisjoint(b))
# True
#-------------------------------------------------------------------------------------------------------------------------------#
# ✅ 16. issubset()
# `a` — `b` ning qismi (subset)mi?
# ✅ 16. issubset()
# Проверяет, является ли a подмножеством b.
a = {1, 2}
b = {1, 2, 3}
print(a.issubset(b))
# True
#-------------------------------------------------------------------------------------------------------------------------------#
# ✅ 17. issuperset()
# `a` — `b` ni to‘liq o‘z ichiga oladimi?
# ✅ 17. issuperset()
# Проверяет, содержит ли a полностью множество b.
a = {1, 2, 3}
b = {2, 3}
print(a.issuperset(b))
# True
