# {'ism': 'Ali'}
#-------------------------------------------------------------------------------------------------------------------------------#
# ‚úÖ 7. popitem()
# popitem() ‚Üí Oxirgi qo‚Äòshilgan juftlikni o‚Äòchiradi va (kalit, qiymat) juftligini qaytaradi
# ‚úÖ 7. popitem()
# popitem() ‚Üí —É–¥–∞–ª—è–µ—Ç –ø–æ—Å–ª–µ–¥–Ω—é—é –¥–æ–±–∞–≤–ª–µ–Ω–Ω—É—é –ø–∞—Ä—É –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –µ—ë –≤ –≤–∏–¥–µ (–∫–ª—é—á, –∑–Ω–∞—á–µ–Ω–∏–µ)
