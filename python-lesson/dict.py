#-------------------------------------------------------------------------------------------------------------------------------#
# ‚úÖ 2. keys()
# keys() ‚Üí Barcha kalitlarni chiqaradi
# ‚úÖ 2. keys()
# keys() ‚Üí –≤—ã–≤–æ–¥–∏—Ç –≤—Å–µ –∫–ª—é—á–∏
talaba = {'ism': 'Ali', 'yosh': 21}
print(talaba.keys())
