#-------------------------------------------------------------------------------------------------------------------------------#
# ‚úÖ 3. values()
# values() ‚Üí Barcha qiymatlarni chiqaradi
# ‚úÖ 3. values()
# values() ‚Üí –≤—ã–≤–æ–¥–∏—Ç –≤—Å–µ –∑–Ω–∞—á–µ–Ω–∏—è
talaba = {'ism': 'Ali', 'yosh': 21}
