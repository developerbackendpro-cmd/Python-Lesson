# {'ism': 'Ali', 'yosh': 21, 'kurs': 3}
#-------------------------------------------------------------------------------------------------------------------------------#
# ‚úÖ 6. pop()
# pop(key) ‚Üí Kalitga mos juftlikni o‚Äòchiradi va qiymatini qaytaradi
# ‚úÖ 6. pop()
# pop(key) ‚Üí —É–¥–∞–ª—è–µ—Ç –ø–∞—Ä—É, —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â—É—é –∫–ª—é—á—É, –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –µ—ë –∑–Ω–∞—á–µ–Ω–∏–µ
