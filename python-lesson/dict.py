# ‚úÖ 8. setdefault()
# setdefault(key, default) ‚Üí Kalit mavjud bo‚Äòlmasa, yangi qiymat bilan qo‚Äòshadi. Mavjud bo‚Äòlsa, mavjud qiymatni qaytaradi.
# ‚úÖ 8. setdefault()
# setdefault(key, default) ‚Üí –µ—Å–ª–∏ –∫–ª—é—á –æ—Ç—Å—É—Ç—Å—Ç–≤—É–µ—Ç, –¥–æ–±–∞–≤–ª—è–µ—Ç –µ–≥–æ —Å –Ω–æ–≤—ã–º –∑–Ω–∞—á–µ–Ω–∏–µ–º.
# –ï—Å–ª–∏ –∫–ª—é—á —É–∂–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç ‚Äî –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Å—É—â–µ—Å—Ç–≤—É—é—â–µ–µ –∑–Ω–∞—á–µ–Ω–∏–µ.
