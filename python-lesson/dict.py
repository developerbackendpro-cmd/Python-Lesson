talaba.setdefault('yosh', 21)  # 'yosh' mavjud emas, shuning uchun qo‚Äòshiladi / 'yosh' –æ—Ç—Å—É—Ç—Å—Ç–≤—É–µ—Ç, –ø–æ—ç—Ç–æ–º—É –±—É–¥–µ—Ç –¥–æ–±–∞–≤–ª–µ–Ω
print(talaba)
# {'ism': 'Ali', 'yosh': 21}
