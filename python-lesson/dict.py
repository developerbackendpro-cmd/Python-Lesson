# dict_values(['Ali', 21])
#-------------------------------------------------------------------------------------------------------------------------------#
# ‚úÖ 4. items()
# items() ‚Üí Har bir juftlikni (key, value) tarzida chiqaradi
# ‚úÖ 4. items()
# items() ‚Üí –≤—ã–≤–æ–¥–∏—Ç –∫–∞–∂–¥—É—é –ø–∞—Ä—É –≤ —Ñ–æ—Ä–º–∞—Ç–µ (key, value)
