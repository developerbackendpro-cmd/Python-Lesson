#-------------------------------------------------------------------------------------------------------------------------------#
# ‚úÖ 5. update()
# update(dict2) ‚Üí Boshqa lug‚Äòatdagi juftliklarni birinchisiga qo‚Äòshadi (yoki yangilaydi)
# ‚úÖ 5. update()
# update(dict2) ‚Üí –¥–æ–±–∞–≤–ª—è–µ—Ç (–∏–ª–∏ –æ–±–Ω–æ–≤–ª—è–µ—Ç) –ø–∞—Ä—ã –∏–∑ –¥—Ä—É–≥–æ–≥–æ —Å–ª–æ–≤–∞—Ä—è –≤ –ø–µ—Ä–≤—ã–π —Å–ª–æ–≤–∞—Ä—å.
