# üî¢ 8Ô∏è‚É£ memoryview (–ø—Ä–æ—Å–º–æ—Ç—Ä –æ–±—ä–µ–∫—Ç–∞ –≤ –ø–∞–º—è—Ç–∏)
# memoryview ‚Äî –ø–æ–∑–≤–æ–ª—è–µ—Ç —á–∏—Ç–∞—Ç—å –∏–ª–∏ –∏–∑–º–µ–Ω—è—Ç—å –±–∞–π—Ç–æ–≤—ã–π –æ–±—ä–µ–∫—Ç –±–µ–∑ —Å–æ–∑–¥–∞–Ω–∏—è –µ–≥–æ –∫–æ–ø–∏–∏.
# –£—Å–∫–æ—Ä—è–µ—Ç —Ä–∞–±–æ—Ç—É –ø—Ä–∏ –æ–±—Ä–∞–±–æ—Ç–∫–µ –æ—á–µ–Ω—å –±–æ–ª—å—à–∏—Ö —Ñ–∞–π–ª–æ–≤.
#------------------------------------------------------------------------------------------------------------------------------------------#
data = bytearray(b'Python')
m = memoryview(data)
# tobytes() ‚Äì xotirani baytga aylantiradi / tobytes() ‚Äì –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ—Ç –æ–±–ª–∞—Å—Ç—å –ø–∞–º—è—Ç–∏ –≤ –±–∞–π—Ç—ã
print(m.tobytes())
# b'Python'
#------------------------------------------------------------------------------------------------------------------------------------------#
# tolist() ‚Äì ro‚Äòyxat ko‚Äòrinishida qaytaradi / tolist() ‚Äì –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –¥–∞–Ω–Ω—ã–µ –≤ –≤–∏–¥–µ —Å–ø–∏—Å–∫–∞
print(m.tolist())
# [80, 121, 116, 104, 111, 110]
#------------------------------------------------------------------------------------------------------------------------------------------#
# cast() ‚Äì xotira formatini o‚Äòzgartiradi (masalan 'B' = bayt) / cast() ‚Äì –∏–∑–º–µ–Ω—è–µ—Ç —Ñ–æ—Ä–º–∞—Ç –ø—Ä–µ–¥—Å—Ç–∞–≤–ª–µ–Ω–∏—è –ø–∞–º—è—Ç–∏ (–Ω–∞–ø—Ä–∏–º–µ—Ä, 'B' = –±–∞–π—Ç)
print(m.cast('B'))
# <memoryview at 0x...>
#------------------------------------------------------------------------------------------------------------------------------------------#
# readonly ‚Äì obyekt faqat o‚Äòqish uchunmi? / readonly ‚Äì –æ–±—ä–µ–∫—Ç –¥–æ—Å—Ç—É–ø–µ–Ω —Ç–æ–ª—å–∫–æ –¥–ª—è —á—Ç–µ–Ω–∏—è?
print(m.readonly)
# False
#------------------------------------------------------------------------------------------------------------------------------------------#
# format ‚Äì memoryview ichidagi elementlarning turi ('B' = bayt) / format ‚Äì —Ç–∏–ø —ç–ª–µ–º–µ–Ω—Ç–æ–≤ memoryview ('B' = –±–∞–π—Ç)
print(m.format)
# 'B'
#------------------------------------------------------------------------------------------------------------------------------------------#
# itemsize ‚Äì bitta element nechta bayt egallaydi / itemsize ‚Äì —Å–∫–æ–ª—å–∫–æ –±–∞–π—Ç –∑–∞–Ω–∏–º–∞–µ—Ç –æ–¥–∏–Ω —ç–ª–µ–º–µ–Ω—Ç
print(m.itemsize)
# 1
#------------------------------------------------------------------------------------------------------------------------------------------#
# nbytes ‚Äì memoryview ichidagi jami baytlar soni / nbytes ‚Äì –æ–±—â–µ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –±–∞–π—Ç–æ–≤ –≤ memoryview
print(m.nbytes)
# 6
#------------------------------------------------------------------------------------------------------------------------------------------#
# ndim ‚Äì o‚Äòlchamlar soni (memoryview 1D bo‚Äòlsa ‚Üí 1) / ndim ‚Äì –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –∏–∑–º–µ—Ä–µ–Ω–∏–π (–µ—Å–ª–∏ memoryview –æ–¥–Ω–æ–º–µ—Ä–Ω—ã–π ‚Üí 1)
print(m.ndim)
# 1
#------------------------------------------------------------------------------------------------------------------------------------------#
# shape ‚Äì massivning shakli (1D bo‚Äòlsa uzunlik qaytaradi) / shape ‚Äì —Ñ–æ—Ä–º–∞ –º–∞—Å—Å–∏–≤–∞ (–¥–ª—è 1D –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –¥–ª–∏–Ω—É)
print(m.shape)
# (6,)
#------------------------------------------------------------------------------------------------------------------------------------------#
# strides ‚Äì elementdan elementga o‚Äòtishdagi qadam (baytlarda) / strides ‚Äì —à–∞–≥ –ø–µ—Ä–µ—Ö–æ–¥–∞ –º–µ–∂–¥—É —ç–ª–µ–º–µ–Ω—Ç–∞–º–∏ (–≤ –±–∞–π—Ç–∞—Ö)
print(m.strides)
# (1,)
#------------------------------------------------------------------------------------------------------------------------------------------#
# hex() ‚Äì memoryview ichidagi baytlarni 16-lik formatda qaytaradi / hex() ‚Äì –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –±–∞–π—Ç—ã –≤ —à–µ—Å—Ç–Ω–∞–¥—Ü–∞—Ç–µ—Ä–∏—á–Ω–æ–º —Ñ–æ—Ä–º–∞—Ç–µ
print(m.hex())
# '507974686f6e'
#------------------------------------------------------------------------------------------------------------------------------------------#
# obj ‚Äì memoryview qaysi asl obyektga ulanganini ko‚Äòrsatadi / obj ‚Äì –ø–æ–∫–∞–∑—ã–≤–∞–µ—Ç –∏—Å—Ö–æ–¥–Ω—ã–π –æ–±—ä–µ–∫—Ç, –Ω–∞ –∫–æ—Ç–æ—Ä—ã–π —Å–º–æ—Ç—Ä–∏—Ç memoryview
print(m.obj)
# bytearray(b'Python')
#------------------------------------------------------------------------------------------------------------------------------------------#
# len(m) ‚Äì nechta element borligini qaytaradi / len(m) ‚Äì –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —ç–ª–µ–º–µ–Ω—Ç–æ–≤
print(len(m))
# 6
#------------------------------------------------------------------------------------------------------------------------------------------#
# memoryview orqali o‚Äòzgartirish (nusxa olinmaydi, bevosita xotira o‚Äòzgaradi!) / –∏–∑–º–µ–Ω–µ–Ω–∏–µ —á–µ—Ä–µ–∑ memoryview (–º–µ–Ω—è–µ—Ç—Å—è –∏—Å—Ö–æ–¥–Ω—ã–π –æ–±—ä–µ–∫—Ç, –±–µ–∑ –∫–æ–ø–∏—Ä–æ–≤–∞–Ω–∏—è!)
