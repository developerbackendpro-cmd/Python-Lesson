#-----------------------------------------------------------------------------------------------------------------------------------------#
# islower() ‚Äì barcha harflar kichik bo‚Äòlsa True / True –µ—Å–ª–∏ –≤—Å–µ –±—É–∫–≤—ã —Å—Ç—Ä–æ—á–Ω—ã–µ
