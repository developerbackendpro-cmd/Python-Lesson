#-----------------------------------------------------------------------------------------------------------------------------------------#
# isspace() ‚Äì faqat bo‚Äòsh joylardan iboratmi? / —Å–æ—Å—Ç–æ–∏—Ç –ª–∏ —Ç–æ–ª—å–∫–æ –∏–∑ –ø—Ä–æ–±–µ–ª–æ–≤?
ba = bytearray(b"   ")
