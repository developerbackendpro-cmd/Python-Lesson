#-----------------------------------------------------------------------------------------------------------------------------------------#
# split() ‚Äì bo‚Äòlib beradi / —Ä–∞–∑–¥–µ–ª—è–µ—Ç –Ω–∞ —á–∞—Å—Ç–∏
