#-----------------------------------------------------------------------------------------------------------------------------------------#
# upper() ‚Äì kichik harflarni kattaga o‚Äòtkazadi / –¥–µ–ª–∞–µ—Ç –±—É–∫–≤—ã –∑–∞–≥–ª–∞–≤–Ω—ã–º–∏
ba = bytearray(b"abc")
print(ba.upper())  # bytearray(b'ABC')
#-----------------------------------------------------------------------------------------------------------------------------------------#
# lower() ‚Äì katta harflarni kichikka o‚Äòtkazadi / –¥–µ–ª–∞–µ—Ç –±—É–∫–≤—ã —Å—Ç—Ä–æ—á–Ω—ã–º–∏
ba = bytearray(b"ABC")
print(ba.lower())  # bytearray(b'abc')
#-----------------------------------------------------------------------------------------------------------------------------------------#
# isupper() ‚Äì barcha harflar katta bo‚Äòlsa True / True –µ—Å–ª–∏ –≤—Å–µ –±—É–∫–≤—ã –∑–∞–≥–ª–∞–≤–Ω—ã–µ
