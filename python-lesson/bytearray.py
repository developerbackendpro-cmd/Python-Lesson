#-----------------------------------------------------------------------------------------------------------------------------------------#
# title() ‚Äì har bir so‚Äòz bosh harfini katta qiladi / –¥–µ–ª–∞–µ—Ç –∫–∞–∂–¥–æ–µ —Å–ª–æ–≤–æ —Å –∑–∞–≥–ª–∞–≤–Ω–æ–π –±—É–∫–≤—ã
ba = bytearray(b"hello world")
print(ba.title())  # bytearray(b"Hello World")
#-----------------------------------------------------------------------------------------------------------------------------------------#
# decode() ‚Äì baytlarni matnga aylantiradi / –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ—Ç –±–∞–π—Ç—ã –≤ —Ç–µ–∫—Å—Ç
