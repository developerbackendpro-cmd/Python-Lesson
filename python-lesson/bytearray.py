print(bx)   # bytearray(b'python')
#-----------------------------------------------------------------------------------------------------------------------------------------#
# rindex() ‚Äì o‚Äòngdan qidiradi, topmasa xato beradi / –∏—â–µ—Ç —Å–ø—Ä–∞–≤–∞, –ø—Ä–∏ –æ—Ç—Å—É—Ç—Å—Ç–≤–∏–∏ –¥–∞—ë—Ç –æ—à–∏–±–∫—É
ba = bytearray(b"hello")
