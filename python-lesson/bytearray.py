#-----------------------------------------------------------------------------------------------------------------------------------------#
# swapcase() ‚Äì katta ‚Üî kichik harflarni almashtiradi / –º–µ–Ω—è–µ—Ç —Ä–µ–≥–∏—Å—Ç—Ä –±—É–∫–≤
