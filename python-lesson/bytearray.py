print(ba.isnumeric())  # True
#-----------------------------------------------------------------------------------------------------------------------------------------#
# center() ‚Äì matnni markazga qo‚Äòyadi / —Ä–∞–∑–º–µ—â–∞–µ—Ç –ø–æ —Ü–µ–Ω—Ç—Ä—É
ba = bytearray(b"hi")
print(ba.center(6, b'-'))  # bytearray(b'--hi--')
#-----------------------------------------------------------------------------------------------------------------------------------------#
# ljust() ‚Äì chapga tekislab, o‚Äòngiga belgi qo‚Äòshadi / –≤—ã—Ä–∞–≤–Ω–∏–≤–∞–µ—Ç —Å–ª–µ–≤–∞ –∏ –¥–æ–±–∞–≤–ª—è–µ—Ç —Å–ø—Ä–∞–≤–∞ —Å–∏–º–≤–æ–ª—ã
ba = bytearray(b"hi")
print(ba.ljust(5, b'_'))  # bytearray(b'hi___')
#-----------------------------------------------------------------------------------------------------------------------------------------#
# rjust() ‚Äì o‚Äòngga tekislab, chapiga belgi qo‚Äòshadi / –≤—ã—Ä–∞–≤–Ω–∏–≤–∞–µ—Ç —Å–ø—Ä–∞–≤–∞ –∏ –¥–æ–±–∞–≤–ª—è–µ—Ç —Å–ª–µ–≤–∞ —Å–∏–º–≤–æ–ª—ã
ba = bytearray(b"hi")
print(ba.rjust(5, b'_'))  # bytearray(b'___hi')
#-----------------------------------------------------------------------------------------------------------------------------------------#
# zfill() ‚Äì boshiga 0 qo‚Äòshadi / –¥–æ–±–∞–≤–ª—è–µ—Ç –Ω—É–ª–∏ –≤ –Ω–∞—á–∞–ª–æ
