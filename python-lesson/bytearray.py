# append() ‚Äì oxiriga bayt qo‚Äòshadi
ba.append(33)
print(ba)
# bytearray(b'python!')
#-----------------------------------------------------------------------------------------------------------------------------------------#
# pop() ‚Äì oxirgi elementni olib tashlaydi
print(ba.pop())
# 33
#-----------------------------------------------------------------------------------------------------------------------------------------#
# extend() ‚Äì boshqa baytlarni qo‚Äòshadi
ba.extend(b'3.12')
print(ba)
# bytearray(b'python3.12')
#-----------------------------------------------------------------------------------------------------------------------------------------#
# remove() ‚Äì berilgan baytni o‚Äòchiradi
ba.remove(112)  # 112 = 'p'
print(ba)
