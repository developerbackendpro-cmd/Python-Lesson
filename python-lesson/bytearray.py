#-----------------------------------------------------------------------------------------------------------------------------------------#
# startswith() ‚Äì shunday boshlanishini tekshiradi / –ø—Ä–æ–≤–µ—Ä—è–µ—Ç, –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è –ª–∏ —Å —ç—Ç–æ–≥–æ
