#-----------------------------------------------------------------------------------------------------------------------------------------#
# isnumeric() ‚Äì faqat raqam belgimi? / —Å–æ—Å—Ç–æ–∏—Ç –ª–∏ —Ç–æ–ª—å–∫–æ –∏–∑ —Ü–∏—Ñ—Ä?
