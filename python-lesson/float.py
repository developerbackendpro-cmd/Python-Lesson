# float ‚Äî —ç—Ç–æ —Ç–∏–ø –¥–∞–Ω–Ω—ã—Ö –¥–ª—è –¥–µ—Å—è—Ç–∏—á–Ω—ã—Ö (–¥—Ä–æ–±–Ω—ã—Ö) —á–∏—Å–µ–ª.
# –û–Ω —Å–æ–¥–µ—Ä–∂–∏—Ç –º–µ—Ç–æ–¥—ã –¥–ª—è –ø—Ä–µ–¥—Å—Ç–∞–≤–ª–µ–Ω–∏—è —á–∏—Å–ª–∞ –≤ –≤–∏–¥–µ –¥—Ä–æ–±–∏, –ø—Ä–æ–≤–µ—Ä–∫–∏ –µ–≥–æ —Ü–µ–ª–æ—Å—Ç–Ω–æ—Å—Ç–∏ –∏–ª–∏ –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏—è –≤ —à–µ—Å—Ç–Ω–∞–¥—Ü–∞—Ç–µ—Ä–∏—á–Ω—ã–π —Ñ–æ—Ä–º–∞—Ç.
# as_integer_ratio() ‚Äî –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç —á–∏—Å–ª–æ –≤ –≤–∏–¥–µ –¥—Ä–æ–±–∏ (—á–∏—Å–ª–∏—Ç–µ–ª—å, –∑–Ω–∞–º–µ–Ω–∞—Ç–µ–ª—å).
#-------------------------------------------------------------------------------------------------------------------------------#
# as_integer_ratio() metodi 2.5 sonini kasr ko‚Äòrinishida (surat, maxraj) qaytaradi.
# –ú–µ—Ç–æ–¥ as_integer_ratio() –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç —á–∏—Å–ª–æ 2.5 –≤ –≤–∏–¥–µ –¥—Ä–æ–±–∏ (—á–∏—Å–ª–∏—Ç–µ–ª—å, –∑–Ω–∞–º–µ–Ω–∞—Ç–µ–ª—å).
a = 2.5
print(a.as_integer_ratio())
# (5, 2)
#-------------------------------------------------------------------------------------------------------------------------------#
# is_integer() ‚Äì son butun bo‚Äòlsa True qaytaradi
# is_integer() ‚Äî –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç True, –µ—Å–ª–∏ —á–∏—Å–ª–æ —è–≤–ª—è–µ—Ç—Å—è —Ü–µ–ª—ã–º.
print((3.0).is_integer())
# True
#-------------------------------------------------------------------------------------------------------------------------------#
# hex() ‚Äì sonning o‚Äòn oltilik (hexadecimal) ko‚Äòrinishini qaytaradi
# hex() ‚Äî –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç —à–µ—Å—Ç–Ω–∞–¥—Ü–∞—Ç–µ—Ä–∏—á–Ω–æ–µ (hex) –ø—Ä–µ–¥—Å—Ç–∞–≤–ª–µ–Ω–∏–µ —á–∏—Å–ª–∞.
print((10.5).hex())
# '0x1.5000000000000p+3'
#-------------------------------------------------------------------------------------------------------------------------------#
# fromhex() ‚Äì hexdan floatga o‚Äòtkazadi
# fromhex() ‚Äî –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ—Ç —à–µ—Å—Ç–Ω–∞–¥—Ü–∞—Ç–µ—Ä–∏—á–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –≤ —á–∏—Å–ª–æ —Ç–∏–ø–∞ float.
print(float.fromhex('0x1.5000000000000p+3'))
# 10.5
#-------------------------------------------------------------------------------------------------------------------------------#
