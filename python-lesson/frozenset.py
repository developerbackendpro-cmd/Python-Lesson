# True
# üü¢ To‚Äòplamlar bir xil elementlarga ega bo‚Äòlsa, == True beradi.
# üü¢ –ï—Å–ª–∏ –º–Ω–æ–∂–µ—Å—Ç–≤–∞ —Å–æ–¥–µ—Ä–∂–∞—Ç –æ–¥–∏–Ω–∞–∫–æ–≤—ã–µ —ç–ª–µ–º–µ–Ω—Ç—ã, –æ–ø–µ—Ä–∞—Ç–æ—Ä == –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç True.
#-------------------------------------------------------------------------------------------------------------------------------#
# ‚úÖ 13. __ne__(other) ‚Üí != operatori
