# üî¥ Elementlar bir xil bo‚Äòlmasa, != True beradi.
# üî¥ –ï—Å–ª–∏ —ç–ª–µ–º–µ–Ω—Ç—ã —Ä–∞–∑–ª–∏—á–∞—é—Ç—Å—è, –æ–ø–µ—Ä–∞—Ç–æ—Ä != –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç True.
#-------------------------------------------------------------------------------------------------------------------------------#
# ‚úÖ 14. __lt__(other) ‚Üí < operatori (strict subset)
