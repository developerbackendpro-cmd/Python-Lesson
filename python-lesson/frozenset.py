print(fs1.difference(fs2))
# frozenset({1, 3})
# üîç Faqat fs1da bor, boshqalarda yo‚Äòq elementlar.
# üîç –≠–ª–µ–º–µ–Ω—Ç—ã, –∫–æ—Ç–æ—Ä—ã–µ –µ—Å—Ç—å —Ç–æ–ª—å–∫–æ –≤ fs1 –∏ –æ—Ç—Å—É—Ç—Å—Ç–≤—É—é—Ç –≤ –¥—Ä—É–≥–∏—Ö.
#-------------------------------------------------------------------------------------------------------------------------------#
# ‚úÖ 3. intersection(*others)
fs1 = frozenset({1, 2, 3})
fs2 = frozenset({2, 4})
print(fs1.intersection(fs2))
# frozenset({2})
# üîó Umumiy elementlar.
# üîó –û–±—â–∏–µ —ç–ª–µ–º–µ–Ω—Ç—ã.
#-------------------------------------------------------------------------------------------------------------------------------#
# ‚úÖ 4. isdisjoint(other)
fs1 = frozenset({1, 2})
fs2 = frozenset({3, 4})
print(fs1.isdisjoint(fs2))
# True
# ‚ùå Umumiy elementi yo‚Äòqmi?
# ‚ùå –ù–µ—Ç –ª–∏ –æ–±—â–∏—Ö —ç–ª–µ–º–µ–Ω—Ç–æ–≤?
#-------------------------------------------------------------------------------------------------------------------------------#
# ‚úÖ 5. issubset(other)
fs1 = frozenset({1, 2})
