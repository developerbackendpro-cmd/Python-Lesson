print(fs1.issuperset(fs2))
# True
# üîº To‚Äòliq qamrab olganmi?
# üîº –ü–æ–ª–Ω–æ—Å—Ç—å—é –æ—Ö–≤–∞—Ç—ã–≤–∞–µ—Ç –ª–∏?
#-------------------------------------------------------------------------------------------------------------------------------#
# ‚úÖ 7. symmetric_difference(other)
fs1 = frozenset({1, 2, 3})
fs2 = frozenset({2, 4})
print(fs1.symmetric_difference(fs2))
# frozenset({1, 3, 4})
# üîÅ Faqat birida bor, ikkalasida birgalikda bo‚Äòlmaganlar.
# üîÅ –≠–ª–µ–º–µ–Ω—Ç—ã, –∫–æ—Ç–æ—Ä—ã–µ –µ—Å—Ç—å —Ç–æ–ª—å–∫–æ –≤ –æ–¥–Ω–æ–º –º–Ω–æ–∂–µ—Å—Ç–≤–µ –∏ –Ω–µ —è–≤–ª—è—é—Ç—Å—è –æ–±—â–∏–º–∏ –¥–ª—è –æ–±–æ–∏—Ö.
#-------------------------------------------------------------------------------------------------------------------------------#
# ‚úÖ 8. union(*others)
fs1 = frozenset({1, 2})
fs2 = frozenset({2, 3})
print(fs1.union(fs2))
# frozenset({1, 2, 3})
# üîó To‚Äòplamlar birlashmasi.
# üîó –û–±—ä–µ–¥–∏–Ω–µ–Ω–∏–µ –º–Ω–æ–∂–µ—Å—Ç–≤.
#-------------------------------------------------------------------------------------------------------------------------------#
# ‚úÖ 9. __contains__() ‚Üí in operatori
