print(fs1.issubset(fs2))
# True
# üîΩ To‚Äòliq ichida bormi?
# üîΩ –°–æ–¥–µ—Ä–∂–∏—Ç—Å—è –ª–∏ –ø–æ–ª–Ω–æ—Å—Ç—å—é –≤–Ω—É—Ç—Ä–∏?
#-------------------------------------------------------------------------------------------------------------------------------#
# ‚úÖ 6. issuperset(other)
fs1 = frozenset({1, 2, 3})
