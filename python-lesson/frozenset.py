# üîÑ O‚Äòzgarmas nusxasini beradi.
# üîÑ –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç –Ω–µ–∏–∑–º–µ–Ω—è–µ–º—É—é –∫–æ–ø–∏—é.
#-------------------------------------------------------------------------------------------------------------------------------#
# ‚úÖ 2. difference(*others)
fs1 = frozenset({1, 2, 3})
