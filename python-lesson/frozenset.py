# üìà –ï—Å–ª–∏ fs1 –ø–æ–ª–Ω–æ—Å—Ç—å—é —Å–æ–¥–µ—Ä–∂–∏—Ç fs2, —Ç–æ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç—Å—è True.
#-------------------------------------------------------------------------------------------------------------------------------#
