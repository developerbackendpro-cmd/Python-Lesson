# –ó–¥–µ—Å—å frozenset({1, 2, 3}) ‚Äî —ç—Ç–æ –Ω–µ–∏–∑–º–µ–Ω—è–µ–º–æ–µ –º–Ω–æ–∂–µ—Å—Ç–≤–æ, —Å–æ—Å—Ç–æ—è—â–µ–µ –∏–∑ 3 —ç–ª–µ–º–µ–Ω—Ç–æ–≤.
#-------------------------------------------------------------------------------------------------------------------------------#
# ‚úÖ 12. __eq__(other) ‚Üí == operatori
