print(2 in fs)
# True
# üîç Element bor-yo‚Äòqligini tekshiradi.
# üîç –ü—Ä–æ–≤–µ—Ä—è–µ—Ç, —Å—É—â–µ—Å—Ç–≤—É–µ—Ç –ª–∏ —ç–ª–µ–º–µ–Ω—Ç.
#-------------------------------------------------------------------------------------------------------------------------------#
# ‚úÖ 10. __len__() ‚Üí len()
