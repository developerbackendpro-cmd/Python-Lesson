# frozenset ‚Äî bu set kabi elementlar to‚Äòplami, lekin o‚Äòzgartirib bo‚Äòlmaydigan (immutable).
# Ya‚Äôni add(), remove() kabi metodlari yo‚Äòq.
# frozenset ‚Äî —ç—Ç–æ –Ω–∞–±–æ—Ä —ç–ª–µ–º–µ–Ω—Ç–æ–≤, –ø–æ—Ö–æ–∂–∏–π –Ω–∞ set, –Ω–æ –µ–≥–æ –Ω–µ–ª—å–∑—è –∏–∑–º–µ–Ω—è—Ç—å (immutable).
# –¢–æ –µ—Å—Ç—å –º–µ—Ç–æ–¥—ã —Ç–∏–ø–∞ add(), remove() –æ—Ç—Å—É—Ç—Å—Ç–≤—É—é—Ç.
#-------------------------------------------------------------------------------------------------------------------------------#
# ‚úÖ 1. copy()
fs = frozenset({1, 2, 3})
print(fs.copy())
