# üî¢ int ‚Äî butun sonlar (integer)
# Python'da butun sonlar uchun 4 ta metod mavjud.
# int ‚Äî bu butun qiymatlarni ifodalaydigan ma‚Äôlumot turi.
# U sonlarning uzunligini bitlarda aniqlash, baytga o‚Äòtkazish, baytdan qaytarish kabi metodlarga ega.
# üî¢ int ‚Äî —Ü–µ–ª—ã–µ —á–∏—Å–ª–∞ (integer)
# –í Python –¥–ª—è —Ü–µ–ª—ã—Ö —á–∏—Å–µ–ª —Å—É—â–µ—Å—Ç–≤—É–µ—Ç 4 –º–µ—Ç–æ–¥–∞.
# int ‚Äî —ç—Ç–æ —Ç–∏–ø –¥–∞–Ω–Ω—ã—Ö, –∫–æ—Ç–æ—Ä—ã–π –ø—Ä–µ–¥—Å—Ç–∞–≤–ª—è–µ—Ç —Ü–µ–ª—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è.
# –û–Ω –∏–º–µ–µ—Ç –º–µ—Ç–æ–¥—ã –¥–ª—è –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è –¥–ª–∏–Ω—ã —á–∏—Å–ª–∞ –≤ –±–∏—Ç–∞—Ö, –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏—è –≤ –±–∞–π—Ç—ã –∏ –æ–±—Ä–∞—Ç–Ω–æ–≥–æ –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏—è –∏–∑ –±–∞–π—Ç–æ–≤.
#-------------------------------------------------------------------------------------------------------------------------------#
# Bu kod 5.9 o‚Äònlik sonini int funksiyasi yordamida butun songa aylantiradi.
# int() funksiyasi faqat butun qismni oladi, kasr qismini tashlab yuboradi.
# –≠—Ç–æ—Ç –∫–æ–¥ –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ—Ç —á–∏—Å–ª–æ 5.9 –≤ —Ü–µ–ª–æ–µ —Å –ø–æ–º–æ—â—å—é —Ñ—É–Ω–∫—Ü–∏–∏ int().
# –§—É–Ω–∫—Ü–∏—è int() –±–µ—Ä—ë—Ç —Ç–æ–ª—å–∫–æ —Ü–µ–ª—É—é —á–∞—Å—Ç—å —á–∏—Å–ª–∞ –∏ –æ—Ç–±—Ä–∞—Å—ã–≤–∞–µ—Ç –¥—Ä–æ–±–Ω—É—é —á–∞—Å—Ç—å.
x = int(5.9)
print(x)
# 5
#-------------------------------------------------------------------------------------------------------------------------------#
# bit_length() ‚Äì sonni ifodalash uchun nechta bit kerakligini qaytaradi
# bit_length() ‚Äî –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç, —Å–∫–æ–ª—å–∫–æ –±–∏—Ç —Ç—Ä–µ–±—É–µ—Ç—Å—è –¥–ª—è –ø—Ä–µ–¥—Å—Ç–∞–≤–ª–µ–Ω–∏—è —á–∏—Å–ª–∞.
x = 15
print(x.bit_length())
# 4  (15 = 1111 ‚Üí 4 bit)
#-------------------------------------------------------------------------------------------------------------------------------#
# to_bytes(length, byteorder) ‚Äì sonni bayt ko‚Äòrinishiga o‚Äòtkazadi
# to_bytes(length, byteorder) ‚Äî –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ—Ç —á–∏—Å–ª–æ –≤ –±–∞–π—Ç–æ–≤–æ–µ –ø—Ä–µ–¥—Å—Ç–∞–≤–ª–µ–Ω–∏–µ.
y = 300
print(y.to_bytes(2, 'big'))
# b'\x01,'
#-------------------------------------------------------------------------------------------------------------------------------#
# from_bytes(bytes, byteorder) ‚Äì baytlardan son hosil qiladi
# from_bytes(bytes, byteorder) ‚Äî —Å–æ–∑–¥–∞—ë—Ç —á–∏—Å–ª–æ –∏–∑ –±–∞–π—Ç–æ–≤.
b = (1).to_bytes(2, 'big')
print(int.from_bytes(b, 'big'))
# 1
